  public class TargetGroupData{
      int id;
      float x;
      float y;
      float width;
      float height;
      float alpha;
      int type;
    
      TargetGroupData(int id, float x, float y, float width, float height, float alpha, int type){
          this.id = id;
          this.x = x;
          this.y = y;
          this.width = width;
          this.height = height;
          this.alpha = alpha;
          this.type = type;
      }
  }
